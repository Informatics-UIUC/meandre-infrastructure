package org.meandre.client;

public class TransmissionException extends Exception {

    private static final long serialVersionUID = 6312322678217327472L;

    public TransmissionException() {
        super();
        
    }

    public TransmissionException(String arg0, Throwable arg1) {
        super(arg0, arg1);
        
    }

    public TransmissionException(String arg0) {
        super(arg0);
        
    }

    public TransmissionException(Throwable arg0) {
        super(arg0);
        
    }

    
}
